"use strict";
/* 
Створити функцію для розрахунку добутку двох чисел, що викликається так: name(5)(2).
Функція повинна повертати результат (у середині функції не має бути консоль лога!)
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
*/

function curry(x){
  return function(y){
      return x + y;
  }
}

const sum = curry(15)(5);
console.log(sum);
